#include "so_long.h"

char	*ft_strjoin(char *s1, char *s2)
{
	char	*res;
	size_t	len;
	int		i;
	int		j;

	i = -1;
	j = -1;
	if ((!s1) || (!s2))
		return (0);
	len = ft_strlen(s1) + ft_strlen(s2);
	res = (char *)malloc((len + 1) * sizeof(char));
	if (!res)
		return (0);
	while (s1[++i])
		res[i] = s1[i];
	while (s2[++j])
		res[i + j] = s2[j];
	res[i + j] = '\0';
	return (res);
}
